/*map의 멤버

size : 맵 컬렉션의 크기 반환
keys : 이 프로퍼티는 Set의 모든 키 반환
values : 이 프로퍼티는 Set의 모든 값을 반환
isEmpty() : 맵이 비어 있는지 확인하고 비어있으면 true를, 아니면 false를 반환
containsKey(key:K) : 인자에 해당하는 키가 있는지 판별
containsValue(value : V) : 인자에 해당하는 값이 있는지 판별
get(key : K) : 키에 해당하는 값 반환, 없으면 null반환


 MutableMap

 put(key : K, value : V) : 키와 값의 쌍을 맵에 추가
 remove(key : K) : 키에 해당하는 요소를 맵에서 제거
 putAll(from : Map<out K,V>) :  인자로 주어진 Map 데이터를 갱신하거나 추가
 clear() : 모든 요소 삭제
 */